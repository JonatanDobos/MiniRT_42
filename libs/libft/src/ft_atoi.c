#include "../libft.h"

int	ft_atoi(const char *c)
{
	int	sign;
	int	num;

	sign = 1;
	num = 0;
	while (*c == ' ' || (*c >= 9 && *c <= 13))
		c++;
	if (*c == '-' || *c == '+')
	{
		if (*c == '-')
			sign = -sign;
		c++;
	}
	while (*c >= '0' && *c <= '9')
		num = (num * 10) + (*c++ - '0');
	return (sign * num);
}
